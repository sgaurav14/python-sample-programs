print(title.upper()) # convert the lower case to the upper case and leave upper case as it is and stored in different variable
print(title)
print(title.lower()) # convert the upper case to lower case into a new var
print(title.find('o')) # find the given value index in the string and find method is case sensitive
print(title.replace('Devops', 'SRE')) # Replace the character or string and it is also case sensitive
print('Devops' in title) # check the expression in there in string or not and it return a boolean value true or false
